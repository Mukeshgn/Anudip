#Q1. Calculate the multiplication and sum of two numbers 

# Declaring an integer variable named 'first_number' and 'second_number'
first_number=int(input("Enter the 1st number: "))
second_number=int(input("Enter the 2nd number: "))

# Logic 1: Calculate the multiplication of the two numbers
multiplication_result=first_number*second_number
# Logic 2: Calculate the sum of the two numbers
sum_result=first_number+second_number

# Display the results of multiplication and sum
print(f"The multiplication of {first_number} and {second_number} is {multiplication_result}")
print(f"The sum of {first_number} and {second_number} is {sum_result}")

