#Q3. Python program to convert the temperature in degree centigrade to Fahrenheit 

# Declaring an float variable named 'celsius_temperature'
celsius_temperature=float(input("Enter the temperature in degree centigrade: "))

# Logic: Convert the temperature from Celsius to Fahrenheit using the formula
fahrenheit_temperature=(celsius_temperature*9/5)+32

# Display the converted temperature in Fahrenheit
print(f"The temperature in {celsius_temperature} degree centigrade is equal to {fahrenheit_temperature} Fahrenheit") 



