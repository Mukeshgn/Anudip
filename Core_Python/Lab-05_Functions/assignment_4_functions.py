#Q4. Accept a name from the user and display that in lower case using lower() function


# Input: Declaring an float variables named 'user_name'
user_name=input("Enter the Name: ")

# Converting the name to lowercase using lower() function
lowercase_name=user_name.lower()

# Displaying the result
print(f"{user_name} in lower case is {lowercase_name}")



