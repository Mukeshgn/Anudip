""" Q1. Write a Python program to find the number of times 4 appears in the tuple. 

Input: 

tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )
"""

# Input: Given tuple
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 )
# Logic: Using the count() method to find how many times 4 appears in the tuple
count=tuplex.count(4)
# Output: Displaying the result
print(f"The number of times 4 appears in the tuple is {count}")


"""
OUTPUT:
The number of times 4 appears in the tuple is 3
"""
