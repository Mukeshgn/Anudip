"""Q2.Write a Python program to convert a list to a tuple.

 Input:

 listx = [5, 10, 7, 4, 15, 3]

"""

# Input: A list of number
listx = [5, 10, 7, 4, 15, 3]

# Logic: Convert the list to a tuple using the tuple() function
tuplex = tuple(listx)

# Output: Display the original list and the converted tuple
print(f"Converted a {listx} list to a tuple: {tuplex}")

