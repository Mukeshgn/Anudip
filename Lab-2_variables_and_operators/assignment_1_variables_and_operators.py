#Q1. Calculate the multiplication and sum of two numbers 

#input
number_1=int(input("Enter the 1st number: "))
number_2=int(input("Enter the 2nd number: "))

#Logic_1(multiplication)
multi=number_1*number_2
#Logic_2(sum)
add=number_1+number_2

#Output
print(f"The multiplication of {number_1} and {number_2} is {multi}")
print(f"The sum of {number_1} and {number_2} is {add}")
