#Q3. Python program to convert the temperature in degree centigrade to Fahrenheit 

#input
degree=float(input("Enter the temperature in degree centigrade: "))

#Logic(convert the temperature in degree centigrade to Fahrenheit)
fahrenheit=(degree*9/5)+32

#Output
print(f"The temperature in {degree} degree centigrade is equal to {fahrenheit} Fahrenheit") 
