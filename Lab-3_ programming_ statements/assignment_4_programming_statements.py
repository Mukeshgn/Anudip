#Q4. Find the Simple Interest on Rs. 200 for 5 years at 5% per year.

#Given inputs in question
principal=200
rate_of_interest=5
time_in_years=5

#Logic(Simple Interest)
simple_interest=(principal*rate_of_interest*time_in_years)/100

#Output
print(f"The Simple Interest on Rs. {principal} for {time_in_years} years at {rate_of_interest}% per year is {simple_interest}.")
